package Day22_Review;

public class Alfabe {

    public static void main(String[] args) {

        // While loop kullanarak alfabenin tum harlerini aralarina bosluk koyarak bastiriniz

        char harf = 'A';

        System.out.println("Alfabe");

        while(harf <= 'Z'){

            System.out.print(harf + " ");

            harf++;
        }

    }
}
