package Day3_MyFirstJavaProgram;

public class EscapeSequence {

    public static void main(String[] args) {

        //insert tab
        System.out.println("Ahmet\tYilmaz");

        //insert line
        System.out.println("Merhaba\nNasilsin");

        //insert single quote
        System.out.println("Ayse\'nin kazagi");

        //insert double quote
        System.out.println("Sampiyon \"Besiktas\"");

        //insert slash
        System.out.println("Sen\\Ben\\O\\Biz\\Siz\\Onlar");

    }
}
